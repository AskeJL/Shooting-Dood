package shoot.doode.commonenemy;

/**
 *
 * @author sande
 */
public class Rectangle {
    float x;
    float y;
    float width;
    float height;
    
    public Rectangle() {
        
    }
    
    public Rectangle(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
